let age ="Prajjawal"; // This is a string literal assigned to the variable age
let isStudent = true; // This is a boolean literal assigned to the variable isStudent
let pie = 3.14; // This is a number literal assigned to the variable pie
let name = "Rai"; // This is a string literal assigned to the variable name
let nullValue = null; // This is a null literal assigned to the variable nullValue
let undefinedValue = undefined; // This is an undefined literal assigned to the variable undefinedValue

// typeof operator is used to check the type of the variable
console.log(typeof age); // string
console.log(typeof isStudent); // boolean
console.log(typeof pie); // number
console.log(typeof name); // string
console.log(typeof nullValue); // object
console.log(typeof undefinedValue); // undefined