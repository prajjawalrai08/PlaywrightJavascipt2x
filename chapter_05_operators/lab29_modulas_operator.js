// % = Modulus
//a%b-->it will reurn the remainder of a/b

let result = 10 % 3; // result will be 1 because 10 divided by 3 leaves a remainder of 1
console.log(result); // 1

console.log(101%2); // 1, because 101 is odd, so the remainder when divided by 2 is 1
console.log(100%2); // 0, because 100 is even, so the remainder when divided by 2 is 0
console.log(37%2); // 1, because 37 is odd, so the remainder when divided by 2 is 1
console.log(36%2); // 0, because 36 is even, so the remainder when divided by 2 is 0

